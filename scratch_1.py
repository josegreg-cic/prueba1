### funciones elementales

def sumar(num1, num2):
    return float(num1) + float(num2)

def restar(num1, num2):
    return float(num1) -float(num2)

def multiplicar(num1,num2):
    return float(num1)*float(num2)

def dividir(num1,num2):
    return float(num1)/float(num2)


print(sumar(2,1))
print(multiplicar(3,5))
print(dividir("3","2"))